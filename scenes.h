#include <iostream>
#include <string>

class Scene
{
    std::string frame;
    std::string text;

private:
public:
    Scene(std::string f, std::string t)
    {
        frame = f;
        text = t;
    }
    void play()
    {
        std::cout << frame << "\n\n"
                  << text;
    }
};

